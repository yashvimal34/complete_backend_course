// Strings

const myVariabe = "Hello World";

// The Length Property
console.log(myVariabe.length); // Outputs: 11
console.log("This is my first javascript tutorial".length); // Outputs: 36

// chatAt() Method
console.log(myVariabe.charAt(6)); // Outputs: H

// indexOf() Method
console.log(myVariabe.indexOf("Wor")); // Outputs: 6

// slice() method
console.log(myVariabe.slice(0, 5));

// toUpperCase() method
console.log(myVariabe.toUpperCase()); // Outputs: HELLO WORLD

// toLowerCase() method
console.log(myVariabe.toLowerCase()); // Outputs: hello world

// includes() method
console.log(myVariabe.includes("World")); // Outputs: true

// split method
console.log(myVariabe.split("d")); // Outputs: [ 'Hello', 'World' ]